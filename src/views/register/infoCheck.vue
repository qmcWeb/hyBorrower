<template>
  <div id="infoCheck">
      <div class="main">
        <div class="top">
          <div class="left">
            短信验证码已发送到
          </div>
          <div class="center">
            {{ telphoneNum }}
          </div>
        </div>
        <div class="check_input login_input_box">
          <input maxlength="6" @keyup="checkNumMethod" ref="check_input"  v-model="checkNumCon" class="login_input" type="text" placeholder="短信验证码">
          <span @click="getAgainCount" class="show_check_num">{{ countNum }}</span>
        </div>
        <div :class="{login_btn: true, active_btn: activeBtn}" @click="loginMethod">
          <span>完成</span>
        </div>
        
        <div v-show="errShow" class="err_message">
          {{ errMessage }}
        </div>
        
      </div>
  </div>
</template>
<script>
import { mapActions, mapState } from 'vuex'
export default {
  name: 'infoCheck',
  data () {
      return {
          telphoneNum: '185****0000',
          checkNumCon: '',
          errMessage: '手机号输入错误',
          errShow: false,
          checkNum: false,
          activeBtn: false,
          countNum: '60s'
      }
  },
  computed: {
    ...mapState([
      'registerUser'
    ])
  },
  mounted(){
    this.getCountDown()
    this.getInfo()
  },
  methods: {
    ...mapActions([
      'changeLoading'
    ]),
    checkNumMethod() {
      if(this.checkNumCon.length === 6){
        this.checkNum = true
        this.errShow = false
      }else{
        this.checkNum = false
        this.errShow = true
        this.errMessage = '验证码不正确'
      }
      if(this.checkNum){
        this.activeBtn = true
      }else{
        this.activeBtn = false
      }
    },
    loginMethod() {
      console.log(this.registerUser)
      if(this.activeBtn){
        this.$store.commit('changeLoading', true)
        this.$http({
          method: 'post',
          url: this.api + '/app/platform/registTwo',
          withCredentials: true,
          params: {
            mobileNum: this.registerUser.mobile,
            password: this.registerUser.password,
            authCode: this.checkNumCon,
            source: 'huiyin'
          }
        }).then((data) => {
          console.log(data)
          if(data.data && data.data.code === '200'){
            this.$router.push('/openAccount')
            this.$store.commit('changeUser', Object.assign({}, data.data.dataBody, {mobile: this.registerUser.mobile}))
          }else{
            this.errShow = true
            this.errMessage = data.data.message
            this.$store.commit('changeLoading', false)
          }
        }).catch(err => {
          console.log(err)
          this.$store.commit('changeLoading', false)
        })
        
      }
    },
    getCountDown() {
      var that = this
      this.commonJs.countDown({
        func: function(){
          if(this.count > 0){
            that.countNum = this.count + 's'
          }else{
            that.countNum = '重新获取'
          }
        },
        count: 60,
        delay: 1000
      })
    },
    getAgainCount() {
      if(this.countNum === '重新获取'){
        this.getCountDown()
        this.getInfoAgain()
      }
    },
    getInfoAgain(){
      this.$http({
        method: 'post',
        url: this.api + '/app/platform/registOne',
        params: {
          mobileNum: this.registerUser.mobile,
          password: this.registerUser.password,
          captchaImg: this.registerUser.captchamImg
        },
        withCredentials: true
      }).then((data) => {
        console.log(data)
        
      })
    },
    getInfo() {
      var str = this.$route.query.telNum.substring(3,7)
      this.telphoneNum = this.$route.query.telNum.replace(str, '****')
      this.$store.commit('changeLoading', false)
    }
    
  }
}
</script>
